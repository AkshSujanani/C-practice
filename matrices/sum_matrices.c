void sum_arr(int arr[][6], int arr2[][6]){
    int i,j,sum[6][6];
    for(i=0; i<6; i++)
        for(j=0;j<6;j++)
            sum[i][j] = arr[i][j]+arr2[i][j];

    for(i=0; i<6; i++){
        for(j=0; j<6; j++)
            printf("%d\t",sum[i][j]);
        printf("\n");
    }
}

